#Here we would be looking into tuples in python
#Tuples are similar to lists, but they are immutable, meaning they cannot be changed after creation.
#We can create a tuple by using parentheses instead of square brackets.
cars_tuple = ("Toyota", "Honda", "Ford", "Chevrolet", "Nissan")
print("Here is the tuple of cars:")
print(cars_tuple)  # Printing the tuple of cars
print(cars_tuple[2])
cars_tuple[2] = ("ademola")